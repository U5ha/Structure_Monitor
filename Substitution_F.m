function [F_value] = Substitution_F(z_hat,force)
y_1=z_hat(1,1);
y_2=z_hat(2,1);
yd_1=z_hat(3,1);
yd_2=z_hat(4,1);
k1=z_hat(5,1);
k2=z_hat(6,1);
c1=z_hat(7,1);
c2=z_hat(8,1);
ugdd1=force(1,1);
ugdd2=force(2,1);
F_value(1,1)=y_1 - ugdd1/80000 + yd_1/200 + (c2*yd_2)/800000 + (k2*y_2)/800000 - (yd_1*(c1/10 + c2/10))/80000 - (y_1*(k1/10 + k2/10))/80000;
F_value(2,1)=y_2 - ugdd2/80000 + yd_2/200 + (c2*yd_1)/800000 - (c2*yd_2)/800000 + (k2*y_1)/800000 - (k2*y_2)/800000;
F_value(3,1)=yd_1 - ugdd1/200 - ((y_1 + (3*yd_1)/800)*(k1/10 + k2/10))/300 + (c2*yd_2)/6000 + (k2*y_2)/6000 + ((c1/10 + c2/10)*((3*ugdd1)/800 - yd_1 - (3*c2*yd_2)/8000 - (3*k2*y_2)/8000 + (3*yd_1*(c1/10 + c2/10))/800 + (3*y_1*(k1/10 + k2/10))/800))/300 + (k2*(y_2 + (3*yd_2)/800))/3000 - (yd_1*(c1/10 + c2/10))/600 - (y_1*(k1/10 + k2/10))/600 - (c2*((3*ugdd2)/800 - yd_2 - (3*c2*yd_1)/8000 + (3*c2*yd_2)/8000 - (3*k2*y_1)/8000 + (3*k2*y_2)/8000))/3000;
F_value(4,1)=yd_2 - ugdd2/200 + (c2*yd_1)/6000 - (c2*yd_2)/6000 + (k2*y_1)/6000 - (k2*y_2)/6000 + (k2*(y_1 + (3*yd_1)/800))/3000 - (k2*(y_2 + (3*yd_2)/800))/3000 + (c2*((3*ugdd2)/800 - yd_2 - (3*c2*yd_1)/8000 + (3*c2*yd_2)/8000 - (3*k2*y_1)/8000 + (3*k2*y_2)/8000))/3000 - (c2*((3*ugdd1)/800 - yd_1 - (3*c2*yd_2)/8000 - (3*k2*y_2)/8000 + (3*yd_1*(c1/10 + c2/10))/800 + (3*y_1*(k1/10 + k2/10))/800))/3000;
F_value(5,1)=k1;
F_value(6,1)=k2;
F_value(7,1)=c1;
F_value(8,1)=c2;
end